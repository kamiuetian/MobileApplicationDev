// double equal vs triple equal
let x = 10;
let y = "10";
console.log(x == y); //only value is checked
console.log(x === y); //value and type is checked
console.log(typeof x);
let z = new Number(10); //built in object
console.log(typeof z);
//object data type
let a = {}; //user defined object
console.log(typeof a);
//array data type
let arr = [];
console.log(typeof arr);
//Null data type
let b = null;
console.log(typeof b);
